# ฟังก์ชันคำนวณความสว่าง (luminance)
def brightness(rgb):
    r, g, b = rgb
    return 0.299*r + 0.587*g + 0.114*b  # สูตรมาตรฐาน Y'

# sort ตามความสว่างจากมากไปน้อย
sorted_colors = sorted(colors.items(), key=lambda x: brightness(x[1]), reverse=True)

fig, ax = plt.subplots(figsize=(10, 15))

for i, (name, rgb) in enumerate(sorted_colors):
    hex_color = "#{:02x}{:02x}{:02x}".format(*rgb)
    ax.add_patch(plt.Rectangle((0, i), 1, 1, color=hex_color))
    ax.text(1.1, i + 0.5, f"{name} {rgb}", va="center", fontsize=10)

ax.set_xlim(0, 3)
ax.set_ylim(0, len(sorted_colors))
ax.axis("off")
plt.show()
